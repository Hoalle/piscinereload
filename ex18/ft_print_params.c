#include <unistd.h>

int		ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int ac, char **av)
{
	int i;

	i = 2;
	while (i <= ac)
	{
		ft_putstr(av[i - 1]);
		ft_putchar('\n');
		i++;
	}
	return (0);
}
